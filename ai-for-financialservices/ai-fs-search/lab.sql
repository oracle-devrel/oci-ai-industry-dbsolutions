                         "documents": [  { 
                            "key": "1x",
                            "text": "'||l_input||'",
                            "languageCode": "auto"  
                          } ],
                          "targetLanguageCode": "'||l_target_lang||'" ,
                           "compartmentId": "ocid1.compartment.oc1..aaaaaaaaud6tkdn6n23cbvc4hexs6n4hggetkwo4viqyneyroixcmj54u32q"
                 
        }';    
         
