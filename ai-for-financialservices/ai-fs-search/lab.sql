            FROM   JSON_TABLE(l_response_json, '$' 
                     COLUMNS (
                                text VARCHAR2(4000)  
                                PATH '$.documents[0].translatedText',
                                src_lang VARCHAR2(20)  
                                PATH '$.documents[0].sourceLanguageCode', 
